public class Setlist {
    private String setlistName;
    private String setlistGenre;
    private String setlistTuning;
    private String setlistDifficulty;

    public String getSetlistName() {
        return setlistName;
    }

    public void setSetlistName(String setlistName) {
        this.setlistName = setlistName;
    }

    public String getSetlistGenre() {
        return setlistGenre;
    }

    public void setSetlistGenre(String setlistGenre) {
        this.setlistGenre = setlistGenre;
    }

    public String getSetlistTuning() {
        return setlistTuning;
    }

    public void setSetlistTuning(String setlistTuning) {
        this.setlistTuning = setlistTuning;
    }

    public String getSetlistDifficulty() {
        return setlistDifficulty;
    }

    public void setSetlistDifficulty(String setlistDifficulty) {
        this.setlistDifficulty = setlistDifficulty;
    }
}
